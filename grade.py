num = int(input("Enter your grade: "))

if 80 <= num <= 100:
    letter = 'A'
elif 70 <= num <= 79:
    letter = 'B'
elif 60 <= num <= 69:
    letter = 'C'
elif 50 <= num <= 59:
    letter = 'D'
else:
    letter = 'F'

print("The letter grade is", letter)
