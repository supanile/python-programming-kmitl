num1 = int(input("Enter a number 1: "))
num2 = int(input("Enter a number 2: "))

if num1 > num2:
    result = num1 + num2
    print(f"The sum of {num1} and {num2} is {result}")
else:
    result = num1 * num2
    print(f"The product of of {num1} and {num2} is {result}")
